    <h5 class="section-title"><i class="fas fa-history"></i> Historical Significance</h5>
    <p>The temple's origins are steeped in legend. It is believed that King Musukunda Chakravarthy, a devotee of Lord Murugan, established this temple after witnessing a divine vision. The temple's history is also intertwined with the worship of Lord Shiva and the establishment of a sacred lingam by the deity Devendra during his penance.</p>

    <h5 class="section-title"><i class="fas fa-archway"></i> Architecture</h5>
    <p>The temple showcases traditional <strong>Dravidian architecture</strong>, featuring a three-tiered rajagopuram (gateway tower) leading to the sanctum. The sanctum houses the image of Balasubramaniyaswamy. Sub-shrines dedicated to Lord Vinayaka and the parents of Balasubramaniyaswamy are also present within the temple complex.</p>

